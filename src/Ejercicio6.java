public class Ejercicio6 {

    public static void main(String[] args) {

        System.out.println("--- Primeros 50 múltiplos de 5 ---");

        // Ciclo for
        for (int i = 1; i <= 50; i++) {

            // Calculamos el múltiplo de 5
            int multiplo = i * 5;

            // Mostramos el resultado
            System.out.println(multiplo);
        }
    }
}